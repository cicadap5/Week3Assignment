public class ThreeAndFive
{
    {
        int number = 5;
        String show="";
        if(number%3==0 && number%5==0){
            show=number+" is divisible by both 3 and 5\n";
        }
        else{
            show=number+" is not divisible by both 3 and 5\n";
        }
        System.out.print(show);
    }
}
